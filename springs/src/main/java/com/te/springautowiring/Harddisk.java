package com.te.springautowiring;

public class Harddisk {
	
	String name;
	String name2;
	@Override
	public String toString() {
		return "Harddisk [name=" + name + ", name2=" + name2 + "]";
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setName2(String name2) {
		this.name2 = name2;
	}
	
	

}
